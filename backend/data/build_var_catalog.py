def _parse_filename(fn: str) -> dict | None:
    """Extract date, slug, folder from a VAR filename."""
    # Strip " (1)", " (2)" copy markers
    fn_clean = re.sub(r"\s*\(\d+\)", "", fn).strip()

    # Match pattern
    m = re.match(
        r"WMT-SEC-VAR-(\d{8})-(.+?)(?:-Detailed|-detailed)(-v\d+)?\.docx$",
        fn_clean,
        re.IGNORECASE,
    )
    if not m:
        # Try simpler pattern: WMT-SEC-VAR-DATE-slug-vN.docx
        m = re.match(
            r"WMT-SEC-VAR-(\d{8})-(.+?)(-v\d+)?\.docx$",
            fn_clean,
            re.IGNORECASE,
        )
        if not m:
            return None

    date_str = m.group(1)
    slug     = m.group(2).strip("-")

    # Determine folder from date prefix
    prefix = date_str[:6]
    sp_folder = FOLDER_MAP.get(prefix)
    if not sp_folder:
        return None

    # Skip known garbage slugs
    slug_lower = slug.lower().replace("-", "").replace("_", "")
    if any(s in slug_lower for s in SKIP_SLUGS_LOWER):
        return None

    report_date = f"{date_str[:4]}-{date_str[4:6]}-{date_str[6:]}"
    sp_url = f"{SP_BASE}/{sp_folder}/{fn_clean}"

    return {
        "filename":    fn_clean,
        "slug":        slug,
        "report_date": report_date,
        "sp_folder":   sp_folder,
        "sharepoint_url": sp_url,
    }


def build_catalog() -> list[dict]:
    """Combine all folder lists, deduplicate by slug (keep most recent),
    and return the catalog."""
    all_files = (
        [(fn, "202511") for fn in NOV_2025]
        + [(fn, "202512") for fn in DEC_2025]
        + [(fn, "202601") for fn in JAN_2026]
        + [(fn, "202602") for fn in FEB_2026]
    )

    # Parse all
    parsed: list[dict] = []
    for fn, _folder in all_files:
        entry = _parse_filename(fn)
        if entry:
            parsed.append(entry)

    # Deduplicate: for the SAME slug + same tech, keep the most recent date
    # Different slugs from the same company (e.g. CrowdStrike-Falcon vs CrowdStrike-XDR)
    # are kept as separate entries — the linker will link them to the same vendor_id
    seen: dict[str, dict] = {}  # slug_lower -> best entry
    for entry in parsed:
        key = entry["slug"].lower().replace("-", "").replace("_", "")
        if key not in seen or entry["report_date"] > seen[key]["report_date"]:
            seen[key] = entry

    catalog = sorted(seen.values(), key=lambda x: x["report_date"], reverse=True)
    return catalog


if __name__ == "__main__":
    catalog = build_catalog()
    OUTPUT.write_text(json.dumps(catalog, indent=2), encoding="utf-8")
    print(f"Catalog saved: {len(catalog)} entries → {OUTPUT}")
